
import { useState } from 'react';
import { useToast } from '@/hooks/use-toast';
import { generateScript } from '@/services/supabaseService';
import { useClinicSegmentation } from './useClinicSegmentation';
import { useDiagnosticPersistence } from './useDiagnosticPersistence';
import { supabase } from '@/integrations/supabase/client';

interface FluidaScriptRequest {
  tema: string;
  equipamentos: string[];
  objetivo?: string;
  mentor?: string;
  formato?: 'carrossel' | 'stories' | 'imagem' | 'reels';
  canal?: string;
  estilo?: string;
}

interface FluidaScriptResult {
  roteiro: string;
  formato: string;
  emocao_central: string;
  intencao: string;
  objetivo: string;
  mentor: string;
  equipamentos_utilizados?: any[];
  canal?: string;
}

// Interface para dados do diagn√≥stico
interface DiagnosticData {
  clinicType?: string;
  medicalSpecialty?: string;
  aestheticFocus?: string;
  medicalBestSeller?: string;
  aestheticBestSeller?: string;
  medicalTicket?: string;
  aestheticTicket?: string;
  targetAudience?: string;
  medicalClinicStyle?: string;
  aestheticClinicStyle?: string;
  communicationStyle?: string;
}

interface EquipmentData {
  id: string;
  nome: string;
  tecnologia: string;
  indicacoes: string;
  beneficios: string;
  diferenciais: string;
  categoria: string;
}

export const useFluidaRoteirista = () => {
  const { toast } = useToast();
  const [isGenerating, setIsGenerating] = useState(false);
  const [results, setResults] = useState<FluidaScriptResult[]>([]);
  const { currentSession } = useDiagnosticPersistence();

  const fetchEquipmentDetails = async (equipmentNames: string[]): Promise<EquipmentData[]> => {
    if (!equipmentNames || equipmentNames.length === 0) {
      return [];
    }

    console.log('üîç [useFluidaRoteirista] Buscando detalhes dos equipamentos:', equipmentNames);

    try {
      const { data: equipments, error } = await supabase
        .from('equipamentos')
        .select('id, nome, tecnologia, indicacoes, beneficios, diferenciais, categoria')
        .in('nome', equipmentNames)
        .eq('ativo', true);

      if (error) {
        console.error('‚ùå [useFluidaRoteirista] Erro ao buscar equipamentos:', error);
        return [];
      }

      if (!equipments || equipments.length === 0) {
        console.warn('‚ö†Ô∏è [useFluidaRoteirista] Nenhum equipamento encontrado para:', equipmentNames);
        return [];
      }

      console.log('‚úÖ [useFluidaRoteirista] Equipamentos encontrados:', equipments.length);
      equipments.forEach(eq => {
        console.log(`üìã [useFluidaRoteirista] ${eq.nome}: ${eq.tecnologia}`);
      });

      return equipments;
    } catch (fetchError) {
      console.error('‚ùå [useFluidaRoteirista] Erro cr√≠tico ao buscar equipamentos:', fetchError);
      return [];
    }
  };

  const generateFluidaScript = async (request: FluidaScriptRequest): Promise<FluidaScriptResult[]> => {
    console.log('üé¨ FLUIDAROTEIRISTA - Iniciando gera√ß√£o', request);
    
    setIsGenerating(true);
    
    try {
      // CR√çTICO: Buscar detalhes dos equipamentos ANTES de gerar
      const equipmentDetails = await fetchEquipmentDetails(request.equipamentos);
      
      if (request.equipamentos.length > 0 && equipmentDetails.length === 0) {
        console.error('‚ùå [useFluidaRoteirista] PROBLEMA CR√çTICO: Equipamentos selecionados mas n√£o encontrados no banco');
        toast({
          title: "‚ö†Ô∏è Equipamentos n√£o encontrados",
          description: `Os equipamentos ${request.equipamentos.join(', ')} n√£o foram encontrados no sistema.`,
          variant: "destructive"
        });
        return [];
      }

      // Usar dados do diagn√≥stico se dispon√≠vel
      const diagnosticData: DiagnosticData = currentSession?.state || {};
      const clinicType = diagnosticData.clinicType || 'estetico';
      
      // Construir contexto enriquecido
      const enrichedContext = {
        tipo_de_clinica: clinicType,
        especialidade: diagnosticData.medicalSpecialty || diagnosticData.aestheticFocus || '',
        equipamentos: equipmentDetails.map(eq => eq.nome).join(', '),
        protocolo: diagnosticData.medicalBestSeller || diagnosticData.aestheticBestSeller || '',
        ticket_medio: diagnosticData.medicalTicket || diagnosticData.aestheticTicket || '',
        publico_ideal: diagnosticData.targetAudience || '',
        estilo_clinica: diagnosticData.medicalClinicStyle || diagnosticData.aestheticClinicStyle || '',
        estilo_linguagem: diagnosticData.communicationStyle || '',
        mentor_nome: request.mentor || 'Criativo'
      };

      // Prompt FLUIDAROTEIRISTA com integra√ß√£o de equipamentos
      const systemPrompt = `
        Voc√™ √© o FLUIDAROTEIRISTA ‚Äî roteirista oficial da plataforma para cl√≠nicas est√©ticas e m√©dicas.
        
        Sua miss√£o √© gerar roteiros criativos, impactantes e prontos para redes sociais.
        
        Contexto da cl√≠nica:
        - Tipo: ${enrichedContext.tipo_de_clinica}
        - Especialidade: ${enrichedContext.especialidade}
        - Equipamentos: ${enrichedContext.equipamentos}
        - Protocolo mais vendido: ${enrichedContext.protocolo}
        - Ticket m√©dio: ${enrichedContext.ticket_medio}
        - P√∫blico ideal: ${enrichedContext.publico_ideal}
        - Estilo da cl√≠nica: ${enrichedContext.estilo_clinica}
        - Mentor: ${enrichedContext.mentor_nome}
        
        ${equipmentDetails.length > 0 ? `
        üö® EQUIPAMENTOS OBRIGAT√ìRIOS (MENCIONE TODOS):
        ${equipmentDetails.map((eq, index) => `${index + 1}. ${eq.nome}: ${eq.tecnologia}
           - Benef√≠cios: ${eq.beneficios}
           - Diferenciais: ${eq.diferenciais}`).join('\n')}
        
        üî• REGRA CR√çTICA: O roteiro DEVE mencionar ESPECIFICAMENTE cada um destes equipamentos pelo nome.
        ‚ö†Ô∏è Se voc√™ n√£o mencionar os equipamentos listados, o roteiro ser√° rejeitado.
        ` : ''}
        
        ESTRUTURA OBRIGAT√ìRIA:
        1. Gancho (capturar aten√ß√£o)
        2. Conflito (apresentar problema)
        3. Virada (mostrar solu√ß√£o com equipamentos espec√≠ficos)
        4. CTA (chamada para a√ß√£o)
        
        FORMATO: ${request.formato || 'carrossel'}
        
        Retorne APENAS JSON v√°lido:
        {
          "roteiro": "Conte√∫do do roteiro",
          "formato": "carrossel/stories/imagem",
          "emocao_central": "esperan√ßa/confian√ßa/urg√™ncia/etc",
          "intencao": "atrair/vender/educar/conectar",
          "objetivo": "Objetivo espec√≠fico do post",
          "mentor": "Nome do mentor usado"
        }
      `;

      const userPrompt = `
        Tema: ${request.tema}
        Canal: ${request.canal || 'instagram'}
        Formato: ${request.formato || 'carrossel'}
        Objetivo: ${request.objetivo || 'atrair'}
        Estilo: ${request.estilo || 'cientifico'}
        Equipamentos: ${equipmentDetails.map(eq => eq.nome).join(', ')}
        
        Crie o roteiro seguindo exatamente as especifica√ß√µes do formato selecionado.
      `;

      console.log('üì§ [useFluidaRoteirista] Enviando para API com equipamentos:', equipmentDetails.map(eq => eq.nome));

      // FIX: Convert equipment array to string for the API call
      const response = await generateScript({
        type: 'fluidaroteirista',
        systemPrompt,
        userPrompt,
        topic: request.tema,
        equipment: equipmentDetails.map(eq => eq.nome).join(', '), // Convert array to comma-separated string
        additionalInfo: JSON.stringify({ 
          ...enrichedContext,
          equipmentDetails // Passar detalhes completos
        }),
        tone: request.estilo || 'professional',
        marketingObjective: request.objetivo as any
      });

      console.log('üì• [useFluidaRoteirista] Resposta recebida:', response);

      // VALIDA√á√ÉO CR√çTICA: Verificar se equipamentos foram mencionados
      if (equipmentDetails.length > 0 && response.content) {
        const equipmentsMentioned = equipmentDetails.filter(eq => 
          response.content.toLowerCase().includes(eq.nome.toLowerCase())
        );
        
        console.log('üîç [useFluidaRoteirista] Verifica√ß√£o de equipamentos:');
        console.log('üìù Esperados:', equipmentDetails.map(eq => eq.nome));
        console.log('‚úÖ Mencionados:', equipmentsMentioned.map(eq => eq.nome));
        
        if (equipmentsMentioned.length < equipmentDetails.length) {
          const missing = equipmentDetails.filter(eq => 
            !equipmentsMentioned.some(mentioned => mentioned.nome === eq.nome)
          );
          
          console.error('‚ùå [useFluidaRoteirista] Equipamentos n√£o mencionados:', missing.map(eq => eq.nome));
          
          toast({
            title: "‚ö†Ô∏è Equipamentos n√£o inclu√≠dos",
            description: `Os equipamentos ${missing.map(eq => eq.nome).join(', ')} n√£o foram mencionados no roteiro. Tentando novamente...`,
            variant: "destructive"
          });
          
          // Por enquanto, vamos continuar mas alertar o usu√°rio
          console.warn('‚ö†Ô∏è [useFluidaRoteirista] Continuando com roteiro incompleto');
        }
      }

      // Tentar parsear como JSON
      let scriptResult: FluidaScriptResult;
      try {
        scriptResult = JSON.parse(response.content);
        scriptResult.equipamentos_utilizados = equipmentDetails;
        scriptResult.canal = request.canal || 'instagram';
      } catch {
        // Fallback se n√£o for JSON v√°lido
        scriptResult = {
          roteiro: response.content,
          formato: request.formato || 'carrossel',
          emocao_central: 'confian√ßa',
          intencao: 'atrair',
          objetivo: request.objetivo || 'Atrair novos clientes',
          mentor: request.mentor || 'Criativo',
          equipamentos_utilizados: equipmentDetails,
          canal: request.canal || 'instagram'
        };
      }

      const results = [scriptResult];
      setResults(results);

      toast({
        title: "üé¨ Roteiro FLUIDA gerado!",
        description: `Criado com ${scriptResult.mentor} - ${scriptResult.formato}${equipmentDetails.length > 0 ? ` (${equipmentDetails.length} equipamento(s))` : ''}`,
      });

      return results;

    } catch (error) {
      console.error('‚ùå Erro no FLUIDAROTEIRISTA:', error);
      
      // Sistema de fallback
      const fallbackScript: FluidaScriptResult = {
        roteiro: `Roteiro n√£o p√¥de ser gerado agora. Suas respostas foram salvas. 
        
        Sugest√£o b√°sica: Fale sobre ${request.tema} e destaque os benef√≠cios √∫nicos dos seus tratamentos${request.equipamentos.length > 0 ? ` com ${request.equipamentos.join(' e ')}` : ''}.`,
        formato: request.formato || 'carrossel',
        emocao_central: 'confian√ßa',
        intencao: 'educar',
        objetivo: 'Manter engajamento',
        mentor: 'B√°sico',
        equipamentos_utilizados: [],
        canal: request.canal || 'instagram'
      };

      setResults([fallbackScript]);

      toast({
        title: "‚ö†Ô∏è Modo Fallback",
        description: "Sistema b√°sico ativado. Tente novamente em instantes.",
        variant: "destructive"
      });

      return [fallbackScript];

    } finally {
      setIsGenerating(false);
    }
  };

  const clearResults = () => {
    setResults([]);
  };

  return {
    generateFluidaScript,
    isGenerating,
    results,
    clearResults
  };
};
